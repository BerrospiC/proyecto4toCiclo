void mostrarclientes()
    {
        String[]titulos={"ID FACTURA","FECHA","RUC","RAZON SOCIAL","TOTAL","ID CLIENTE","ID EMPLEADO"} ;  
        String []Registros= new String[7];
        modelo=new DefaultTableModel(null,titulos);
        String Sql="SELECT * FROM r_factura"; 
       
        try {
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(Sql);
             while(rs.next())
             {
                 Registros[0]=rs.getString("id_factura");  
                 Registros[1]=rs.getString("fecha"); 
                 Registros[2]=rs.getString("ruc"); 
                 Registros[3]=rs.getString("razonsocial"); 
                 Registros[4]=rs.getString("total");  
                 Registros[5]=rs.getString("id_cliente");  
                 Registros[6]=rs.getString("id_empleado");  
                 
                 modelo.addRow(Registros);
             } 
             jtabla.setModel(modelo);
        } catch (SQLException ex) {
           
        }
 }
